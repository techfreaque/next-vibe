import type endpoints from "./definition";
import type { SystemSettingsGetResponseOutput } from "./definition";

type WizardStep = SystemSettingsGetResponseOutput["wizardSteps"][number];
type Module = SystemSettingsGetResponseOutput["modules"][number];
type Setting = Module["settings"][number];

// Group labels matching onboardingGroup values
const GROUP_LABELS: Record<string, string> = {
  admin: "wizard.steps.admin",
  database: "wizard.steps.database",
  security: "wizard.steps.security",
  ai: "wizard.steps.ai",
};

/** Flatten all settings from all modules into a single lookup map */
function buildSettingsMap(modules: Module[]): Map<string, Setting> {
  const map = new Map<string, Setting>();
  for (const mod of modules) {
    for (const s of mod.settings) {
      map.set(s.key, s);
    }
  }
  return map;
}

/** True if this key is auto-generated (JWT / CRON secrets that have a default) */
function isAutoGenerated(key: string): boolean {
  return key === "JWT_SECRET_KEY" || key === "CRON_SECRET";
}

function StepIndicator({
  steps,
  currentStep,
  t,
}: {
  steps: WizardStep[];
  currentStep: number;
  t: (k: string) => string;
}): JSX.Element {
  return (
    <Div className="flex items-center gap-1 mb-6">
      {steps.map((step, idx) => {
        const isActive = idx === currentStep;
        const isDone = idx < currentStep;
        return (
          <React.Fragment key={step.step}>
            <Div className="flex flex-col items-center gap-1">
              <Div
                className={cn(
                  "w-8 h-8 rounded-full flex items-center justify-center text-sm font-bold transition-colors",
                  isActive && "bg-primary text-primary-foreground",
                  isDone && "bg-green-500 text-white",
                  !isActive && !isDone && "bg-muted text-muted-foreground",
                )}
              >
                {isDone ? <Check className="h-4 w-4" /> : step.step}
              </Div>
              <Span
                className={cn(
                  "text-[10px] text-center hidden sm:block",
                  isActive
                    ? "text-primary font-medium"
                    : "text-muted-foreground",
                )}
              >
                {t(GROUP_LABELS[step.group] ?? step.group)}
              </Span>
            </Div>
            {idx < steps.length - 1 && (
              <Div
                className={cn(
                  "flex-1 h-0.5 mb-4",
                  idx < currentStep ? "bg-green-500" : "bg-muted",
                )}
              />
            )}
          </React.Fragment>
        );
      })}
    </Div>
  );
}

function EncryptionCallout({ t }: { t: (k: string) => string }): JSX.Element {
  return (
    <Div className="flex items-start gap-2 px-3 py-2 rounded-md bg-green-50 dark:bg-green-950/20 border border-green-200 dark:border-green-800 text-xs text-green-800 dark:text-green-300 mb-2">
      <ShieldPlus className="h-3.5 w-3.5 shrink-0 mt-0.5" />
      <Span>{t("wizard.encryptionNote")}</Span>
    </Div>
  );
}

function AutoGeneratedCallout({
  t,
}: {
  t: (k: string) => string;
}): JSX.Element {
  return (
    <Div className="flex items-start gap-2 px-3 py-2 rounded-md bg-blue-50 dark:bg-blue-950/20 border border-blue-200 dark:border-blue-800 text-xs text-blue-800 dark:text-blue-300 mb-2">
      <RefreshCw className="h-3.5 w-3.5 shrink-0 mt-0.5" />
      <Span>{t("wizard.autoGeneratedNote")}</Span>
    </Div>
  );
}

function GenerateKeyButton({
  onGenerate,
}: {
  onGenerate: () => Promise<void>;
}): JSX.Element {
  const [loading, setLoading] = useState(false);
  const handleClick = useCallback(async (): Promise<void> => {
    setLoading(true);
    try {
      await onGenerate();
    } finally {
      setLoading(false);
    }
  }, [onGenerate]);

  return (
    <Button
      variant="outline"
      size="sm"
      className="h-7 text-xs"
      disabled={loading}
      onClick={handleClick}
    >
      <RefreshCw className={cn("h-3 w-3 mr-1", loading && "animate-spin")} />
      {loading ? "Generating..." : "Generate"}
    </Button>
  );
}

function ClaudeCodeField({
  setting,
  t,
}: {
  setting: Setting;
  t: (k: string) => string;
}): JSX.Element {
  const isDetected = setting.value === "true" || setting.isConfigured;
  return (
    <Div className="flex flex-col gap-3 mb-4">
      <Div className="flex items-center gap-1.5">
        <Span className="text-sm font-medium">
          {t("wizard.ai.claudeCodeTitle")}
        </Span>
      </Div>
      <Span className="text-xs text-muted-foreground">
        {t("wizard.ai.claudeCodeDescription")}
      </Span>
      {isDetected ? (
        <Div className="flex items-start gap-2 px-3 py-2 rounded-md bg-green-50 dark:bg-green-950/20 border border-green-200 dark:border-green-800 text-xs text-green-800 dark:text-green-300">
          <Check className="h-3.5 w-3.5 shrink-0 mt-0.5" />
          <Span>{t("wizard.ai.claudeDetected")}</Span>
        </Div>
      ) : (
        <Div className="flex items-start gap-2 px-3 py-2 rounded-md bg-amber-50 dark:bg-amber-950/20 border border-amber-200 dark:border-amber-800 text-xs text-amber-800 dark:text-amber-300">
          <AlertCircle className="h-3.5 w-3.5 shrink-0 mt-0.5" />
          <Div className="flex flex-col gap-1">
            <Span className="font-medium">
              {t("wizard.ai.claudeNotDetected")}
            </Span>
            <Span>{t("wizard.ai.claudeInstallHint")}</Span>
          </Div>
        </Div>
      )}
    </Div>
  );
}

function OpenRouterHint({ t }: { t: (k: string) => string }): JSX.Element {
  return (
    <Div className="flex items-start gap-2 px-3 py-2 rounded-md bg-blue-50 dark:bg-blue-950/20 border border-blue-200 dark:border-blue-800 text-xs text-blue-800 dark:text-blue-300 mb-2">
      <AlertCircle className="h-3.5 w-3.5 shrink-0 mt-0.5" />
      <Span>{t("wizard.ai.openRouterHint")}</Span>
    </Div>
  );
}

function WizardField({
  setting,
  editedValue,
  onEdit,
  onGenerate,
  t,
}: {
  setting: Setting;
  editedValue: string | undefined;
  onEdit: (key: string, value: string) => void;
  onGenerate?: (key: string) => Promise<void>;
  t: (k: string) => string;
}): JSX.Element {
  if (setting.key === "CLAUDE_CODE_ENABLED") {
    return <ClaudeCodeField setting={setting} t={t} />;
  }
  const autoGen = isAutoGenerated(setting.key);
  const displayValue =
    editedValue ?? (setting.isConfigured ? setting.value : "");

  return (
    <Div className="flex flex-col gap-1.5 mb-4">
      <Div className="flex items-center justify-between">
        <Div className="flex items-center gap-1.5">
          {setting.isSensitive && (
            <ShieldPlus className="h-3 w-3 text-green-500 shrink-0" />
          )}
          <Span className="text-sm font-medium">{setting.key}</Span>
          {setting.onboardingRequired && (
            <Span className="text-[10px] px-1 py-0.5 rounded bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-300">
              {t("widget.required")}
            </Span>
          )}
        </Div>
        {autoGen && onGenerate && (
          <GenerateKeyButton
            onGenerate={(): Promise<void> => onGenerate(setting.key)}
          />
        )}
      </Div>

      {setting.comment && (
        <Span className="text-xs text-muted-foreground">{setting.comment}</Span>
      )}

      {setting.isSensitive && !autoGen && <EncryptionCallout t={t} />}
      {autoGen && <AutoGeneratedCallout t={t} />}
      {setting.key === "OPENROUTER_API_KEY" && <OpenRouterHint t={t} />}

      {setting.fieldType === "select" && setting.options?.length ? (
        <Select
          value={displayValue}
          onValueChange={(v): void => onEdit(setting.key, v)}
        >
          <SelectTrigger className="h-9 text-sm font-mono">
            <SelectValue placeholder="Select..." />
          </SelectTrigger>
          <SelectContent>
            {setting.options.map((opt) => (
              <SelectItem key={opt} value={opt}>
                {opt}
              </SelectItem>
            ))}
          </SelectContent>
        </Select>
      ) : setting.fieldType === "boolean" ? (
        <Div className="flex items-center gap-2">
          <Switch
            checked={displayValue === "true"}
            onCheckedChange={(v): void =>
              onEdit(setting.key, v ? "true" : "false")
            }
          />
          <Span className="text-xs text-muted-foreground font-mono">
            {displayValue === "true"
              ? t("widget.boolTrue")
              : t("widget.boolFalse")}
          </Span>
        </Div>
      ) : setting.isSensitive ? (
        <Input
          type="password"
          className="h-9 text-sm font-mono"
          value={editedValue ?? ""}
          placeholder={
            setting.isConfigured && setting.value !== "change-me-now"
              ? // eslint-disable-next-line i18next/no-literal-string
                "••••••••"
              : setting.example || setting.comment || undefined
          }
          onChange={(e: InputChangeEvent<"password">): void =>
            onEdit(setting.key, String(e.target.value ?? ""))
          }
        />
      ) : (
        <Input
          className="h-9 text-sm font-mono"
          value={editedValue ?? ""}
          placeholder={setting.example || setting.comment || undefined}
          onChange={(e: InputChangeEvent): void =>
            onEdit(setting.key, String(e.target.value ?? ""))
          }
        />
      )}
    </Div>
  );
}

function DoneScreen({
  onGoToSettings,
  t,
}: {
  onGoToSettings: () => void;
  t: (k: string) => string;
}): JSX.Element {
  return (
    <Div className="flex flex-col items-center gap-4 py-8 text-center">
      <Div className="w-16 h-16 rounded-full bg-green-100 dark:bg-green-900/30 flex items-center justify-center">
        <Check className="h-8 w-8 text-green-600 dark:text-green-400" />
      </Div>
      <Div className="flex flex-col gap-1">
        <Span className="text-xl font-bold">{t("wizard.done")}</Span>
        <Span className="text-sm text-muted-foreground max-w-sm">
          {t("wizard.doneSubtitle")}
        </Span>
      </Div>
      <Button onClick={onGoToSettings}>
        <ChevronRight className="h-4 w-4 mr-1.5" />
        {t("wizard.goToSettings")}
      </Button>
    </Div>
  );
}

interface WizardProps {
  data: SystemSettingsGetResponseOutput;
  onDone: () => void;
}

export function SettingsWizard({ data, onDone }: WizardProps): JSX.Element {
  const t = useWidgetTranslation<typeof endpoints.GET>() as (
    k: string,
  ) => string;
  const locale = useWidgetLocale();
  const user = useWidgetUser();
  const logger = useWidgetLogger();
  const { endpointMutations } = useWidgetContext();

  const { wizardSteps, modules } = data;
  const settingsMap = buildSettingsMap(modules);

  const [currentStep, setCurrentStep] = useState(0);
  const [edits, setEdits] = useState<Record<string, string>>({});
  const [saving, setSaving] = useState(false);
  const [done, setDone] = useState(false);

  const totalSteps = wizardSteps.length;
  const activeWizardStep = wizardSteps[currentStep];
  const stepFields = (activeWizardStep?.fields ?? [])
    .map((key) => settingsMap.get(key))
    .filter((s): s is Setting => s !== undefined);

  const handleEdit = useCallback((key: string, value: string): void => {
    setEdits((prev) => ({ ...prev, [key]: value }));
  }, []);

  const handleGenerate = useCallback(
    async (key: string): Promise<void> => {
      if (!user) {
        return;
      }
      try {
        const { apiClient } =
          await import("@/app/api/[locale]/system/unified-interface/react/hooks/store");
        const generateKeyDef = await import("./generate-key/definition");
        const result = await apiClient.fetch(
          generateKeyDef.default.GET,
          logger,
          user,
          undefined,
          undefined,
          locale,
        );
        if (result.success && result.data) {
          handleEdit(key, result.data.key);
        }
      } catch {
        // ignore
      }
    },
    [user, logger, locale, handleEdit],
  );

  const saveCurrentStep = useCallback(async (): Promise<void> => {
    if (!user) {
      return;
    }
    const stepKeys = activeWizardStep?.fields ?? [];
    const toSave: Record<string, string> = {};
    for (const key of stepKeys) {
      if (edits[key] !== undefined && edits[key] !== "") {
        toSave[key] = edits[key];
      }
    }
    if (Object.keys(toSave).length === 0) {
      return;
    }

    setSaving(true);
    try {
      const { apiClient } =
        await import("@/app/api/[locale]/system/unified-interface/react/hooks/store");
      const endpointsDef = await import("./definition");
      await apiClient.mutate(
        endpointsDef.PATCH,
        logger,
        user,
        // eslint-disable-next-line @typescript-eslint/no-explicit-any
        toSave as any,
        undefined,
        locale,
      );
      endpointMutations?.read?.refetch?.();
    } finally {
      setSaving(false);
    }
  }, [activeWizardStep, edits, user, logger, locale, endpointMutations]);

  const handleNext = useCallback(async (): Promise<void> => {
    await saveCurrentStep();
    if (currentStep < totalSteps - 1) {
      setCurrentStep((s) => s + 1);
    } else {
      setDone(true);
      localStorage.setItem("vibe-wizard-dismissed", "1");
    }
  }, [saveCurrentStep, currentStep, totalSteps]);

  const handleBack = useCallback((): void => {
    setCurrentStep((s) => Math.max(0, s - 1));
  }, []);

  const handleSkip = useCallback((): void => {
    if (currentStep < totalSteps - 1) {
      setCurrentStep((s) => s + 1);
    } else {
      setDone(true);
      localStorage.setItem("vibe-wizard-dismissed", "1");
    }
  }, [currentStep, totalSteps]);

  // Pre-fill auto-generated keys that are already configured
  useEffect(() => {
    for (const step of wizardSteps) {
      for (const key of step.fields) {
        const setting = settingsMap.get(key);
        if (
          setting?.isConfigured &&
          isAutoGenerated(key) &&
          edits[key] === undefined
        ) {
          // Don't pre-fill - they're already set; wizard just confirms
        }
      }
    }
  }, [wizardSteps, settingsMap, edits]);

  if (done) {
    return <DoneScreen onGoToSettings={onDone} t={t} />;
  }

  return (
    <Div className="flex flex-col gap-0">
      <Div className="p-6 pb-2">
        <Div className="mb-1">
          <Span className="text-xl font-bold">{t("wizard.title")}</Span>
        </Div>
        <Span className="text-sm text-muted-foreground">
          {t("wizard.subtitle")}
        </Span>
      </Div>

      <Div className="px-6 pt-4">
        <StepIndicator steps={wizardSteps} currentStep={currentStep} t={t} />
      </Div>

      <Div className="px-6 py-2">
        <Div className="flex items-center justify-between mb-4">
          <Span className="font-semibold text-base">
            {t(
              GROUP_LABELS[activeWizardStep?.group ?? ""] ??
                activeWizardStep?.group ??
                "",
            )}
          </Span>
          <Button
            variant="ghost"
            size="sm"
            className="text-xs h-7"
            onClick={(): void => {
              localStorage.setItem("vibe-wizard-dismissed", "1");
              onDone();
            }}
          >
            {t("wizard.viewAllSettings")}
          </Button>
        </Div>

        {stepFields.map((setting) => (
          <WizardField
            key={setting.key}
            setting={setting}
            editedValue={edits[setting.key]}
            onEdit={handleEdit}
            onGenerate={
              isAutoGenerated(setting.key) ? handleGenerate : undefined
            }
            t={t}
          />
        ))}
      </Div>

      <Div className="flex items-center justify-between px-6 py-4 border-t mt-2">
        {currentStep > 0 ? (
          <Button variant="ghost" size="sm" onClick={handleBack}>
            <ChevronLeft className="h-4 w-4 mr-1" />
            {t("wizard.back")}
          </Button>
        ) : (
          <Div />
        )}

        <Div className="flex items-center gap-2">
          <Button variant="ghost" size="sm" onClick={handleSkip}>
            {t("wizard.skip")}
          </Button>
          <Button size="sm" disabled={saving} onClick={handleNext}>
            {saving ? (
              <RefreshCw className="h-3.5 w-3.5 mr-1.5 animate-spin" />
            ) : currentStep < totalSteps - 1 ? (
              <>
                {t("wizard.next")}
                <ChevronRight className="h-4 w-4 ml-1" />
              </>
            ) : (
              <>
                <Check className="h-3.5 w-3.5 mr-1.5" />
                {t("wizard.finish")}
              </>
            )}
          </Button>
        </Div>
      </Div>
    </Div>
  );
}
