/**
 * Settings Setup Wizard
 * Step-by-step guided configuration for first-time setup.
 * Shown automatically when needsOnboarding=true, or when user clicks "Setup Wizard".
 */

"use client";

import { Button } from "next-vibe-ui/ui/button";
import { Div } from "next-vibe-ui/ui/div";
import { AlertCircle } from "next-vibe-ui/ui/icons/AlertCircle";
import { Check } from "next-vibe-ui/ui/icons/Check";
import { ChevronLeft } from "next-vibe-ui/ui/icons/ChevronLeft";
import { ChevronRight } from "next-vibe-ui/ui/icons/ChevronRight";
import { RefreshCw } from "next-vibe-ui/ui/icons/RefreshCw";
import { ShieldPlus } from "next-vibe-ui/ui/icons/ShieldPlus";
import type { InputChangeEvent } from "next-vibe-ui/ui/input";
import { Input } from "next-vibe-ui/ui/input";
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "next-vibe-ui/ui/select";
import { Span } from "next-vibe-ui/ui/span";
import { Switch } from "next-vibe-ui/ui/switch";
import type { JSX } from "react";
import React, { useCallback, useEffect, useState } from "react";

